  LEFT JOIN [vct].[Rate_Region] r ON r.[MKT_NBR] = d.[MKT_NBR]
  LEFT JOIN  [peg].[PEG_ANCH_UHPD_SOURCE] p ON d.PEG_ANCH_CATGY = p.PEG_ANCH_CATGY AND  d.PEG_ANCH_SBCATGY= p.PEG_ANCH_SBCATGY